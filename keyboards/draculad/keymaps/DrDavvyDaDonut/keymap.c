enum layer_number {
  _BASE,
  _ALT,
  _RAISE,
  _LOWER,
  _ADJ,
  _MOUSE,
  _GAME
};

//  start up  
void keyboard_post_init_user(void){
  rgblight_disable();
  combo_disable();
  set_single_persistent_default_layer(_BASE);
}

//  on layer changes
layer_state_t layer_state_set_user(layer_state_t state) {
  
  if (layer_state_cmp(state, _ADJ)) {
    combo_enable();
  }
  return state;
}

